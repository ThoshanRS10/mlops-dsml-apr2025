def counting_sort_master(arr):
    """Counting Sort Algorithm"""
    if len(arr) == 0:
        return arr
    max_val = max(arr)
    count = [0] * (max_val + 1)
    for num in arr:
        count[num] += 1
    sorted_arr = []
    for i, cnt in enumerate(count):
        sorted_arr.extend([i] * cnt)
    return sorted_arr